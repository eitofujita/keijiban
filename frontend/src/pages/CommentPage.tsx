// src/pages/CommentPage.tsx
import { useParams, Link } from "react-router-dom";
import { useState, useEffect } from "react";
import {
  Avatar,
  Box,
  Typography,
  TextField,
  Button,
  Card,
  CardContent,
  Divider,
  IconButton,
} from "@mui/material";
import ClearIcon from "@mui/icons-material/Clear";
import ThumbUpIcon from "@mui/icons-material/ThumbUp";
import ThumbDownIcon from "@mui/icons-material/ThumbDown";
import ChatBubbleOutlineIcon from "@mui/icons-material/ChatBubbleOutline";

import { db } from "../firebase";
import {
  doc,
  getDoc,
  updateDoc,
  arrayUnion,
  arrayRemove,
  increment,
  collection,
  addDoc,
  serverTimestamp,
  query,
  orderBy,
  onSnapshot,
  deleteDoc,
} from "firebase/firestore";
import { useAuth } from "../hooks/useAuth";
import CommentOptions from "../components/CommentOptions";

export default function CommentPage() {
  const { id } = useParams();
  const { user } = useAuth();

  const [post, setPost] = useState<any>(null);
  const [comment, setComment] = useState("");
  const [comments, setComments] = useState<any[]>([]);
  const [replyingTo, setReplyingTo] = useState<string | null>(null);
  const [replyText, setReplyText] = useState("");

  // üîπ ÊäïÁ®ø„Éá„Éº„ÇøÂèñÂæóÔºà„É™„Ç¢„É´„Çø„Ç§„É†„Å´Â§âÊõ¥„Åó„ÅüÊñπ„Åå„ÅÑ„ÅÑÔºâ
  useEffect(() => {
    if (!id) return;
    const ref = doc(db, "posts", id);
    const unsub = onSnapshot(ref, (snap) => {
      if (snap.exists()) setPost({ id: snap.id, ...snap.data() });
    });
    return () => unsub();
  }, [id]);

  // üîπ „Ç≥„É°„É≥„ÉàÂèñÂæóÔºà„É™„Ç¢„É´„Çø„Ç§„É†Ôºâ
  useEffect(() => {
    if (!id) return;
    const q = query(
      collection(db, "posts", id, "comments"),
      orderBy("createdAt", "asc")
    );
    const unsub = onSnapshot(q, (snapshot) => {
      setComments(snapshot.docs.map((doc) => ({ id: doc.id, ...doc.data() })));
    });
    return () => unsub();
  }, [id]);

  // üîπ ÊäïÁ®ø„Å´ Like
  const handleLikePost = async () => {
    if (!id || !user) return;
    const ref = doc(db, "posts", id);
    if (post.likedBy?.includes(user.uid)) {
      await updateDoc(ref, { likedBy: arrayRemove(user.uid) });
    } else {
      await updateDoc(ref, {
        likedBy: arrayUnion(user.uid),
        dislikedBy: arrayRemove(user.uid),
      });
    }
  };

  // üîπ ÊäïÁ®ø„Å´ Dislike
  const handleDislikePost = async () => {
    if (!id || !user) return;
    const ref = doc(db, "posts", id);
    if (post.dislikedBy?.includes(user.uid)) {
      await updateDoc(ref, { dislikedBy: arrayRemove(user.uid) });
    } else {
      await updateDoc(ref, {
        dislikedBy: arrayUnion(user.uid),
        likedBy: arrayRemove(user.uid),
      });
    }
  };

  // üîπ Êñ∞Ë¶è„Ç≥„É°„É≥„ÉàËøΩÂä†
  const handleComment = async () => {
    if (!user || !comment.trim()) return;
    await addDoc(collection(db, "posts", id!, "comments"), {
      text: comment.trim(),
      username: user.displayName || "ÂêçÁÑ°„Åó",
      avatarUrl: user.photoURL || "",
      uid: user.uid,
      createdAt: serverTimestamp(),
      likedBy: [],
      dislikedBy: [],
      parentId: null,
    });
    const postRef = doc(db, "posts", id!);
    await updateDoc(postRef, { commentsCount: increment(1) });
    setComment("");
  };

  // üîπ Ëøî‰ø°„Ç≥„É°„É≥„ÉàËøΩÂä†
  const handleReplySubmit = async (parentId: string) => {
    if (!user || !replyText.trim()) return;
    await addDoc(collection(db, "posts", id!, "comments"), {
      text: replyText.trim(),
      username: user.displayName || "ÂêçÁÑ°„Åó",
      avatarUrl: user.photoURL || "",
      uid: user.uid,
      createdAt: serverTimestamp(),
      likedBy: [],
      dislikedBy: [],
      parentId,
    });
    setReplyText("");
    setReplyingTo(null);
  };

  // üîπ „Ç≥„É°„É≥„ÉàÂâäÈô§
  const handleDelete = async (commentId: string) => {
    if (!id) return;
    await deleteDoc(doc(db, "posts", id, "comments", commentId));
    const postRef = doc(db, "posts", id);
    await updateDoc(postRef, { commentsCount: increment(-1) });
  };

  // üîπ „Ç≥„É°„É≥„ÉàÁ∑®ÈõÜ
  const handleEdit = async (commentId: string, newText: string) => {
    if (!id || !newText.trim()) return;
    const ref = doc(db, "posts", id, "comments", commentId);
    await updateDoc(ref, { text: newText.trim() });
  };

  // üîπ „Ç≥„É°„É≥„Éà Like
  const handleLikeComment = async (
    commentId: string,
    likedBy: string[] = [],
    dislikedBy: string[] = []
  ) => {
    if (!id || !user) return;
    const ref = doc(db, "posts", id, "comments", commentId);
    if (likedBy.includes(user.uid)) {
      await updateDoc(ref, { likedBy: arrayRemove(user.uid) });
    } else {
      await updateDoc(ref, {
        likedBy: arrayUnion(user.uid),
        dislikedBy: arrayRemove(user.uid),
      });
    }
  };

  // üîπ „Ç≥„É°„É≥„Éà Dislike
  const handleDislikeComment = async (
    commentId: string,
    likedBy: string[] = [],
    dislikedBy: string[] = []
  ) => {
    if (!id || !user) return;
    const ref = doc(db, "posts", id, "comments", commentId);
    if (dislikedBy.includes(user.uid)) {
      await updateDoc(ref, { dislikedBy: arrayRemove(user.uid) });
    } else {
      await updateDoc(ref, {
        dislikedBy: arrayUnion(user.uid),
        likedBy: arrayRemove(user.uid),
      });
    }
  };

  if (!post) return <Typography>Loading...</Typography>;

  // üîπ ÂÜçÂ∏∞ÁöÑ„Å´„Ç≥„É°„É≥„Éà„ÇíÊèèÁîª
  function renderComments(parentId: string | null, level = 0) {
    const items = comments.filter((c) => c.parentId === parentId);
    return items.map((c) => (
      <Box key={c.id} sx={{ ml: level * 4, mt: 1 }}>
        <CommentItem
          c={c}
          user={user}
          onLike={handleLikeComment}
          onDislike={handleDislikeComment}
          onDelete={handleDelete}
          onEdit={handleEdit}
          replyingTo={replyingTo}
          setReplyingTo={setReplyingTo}
          replyText={replyText}
          setReplyText={setReplyText}
          onReplySubmit={handleReplySubmit}
        />
        {renderComments(c.id, level + 1)}
      </Box>
    ));
  }

  return (
    <Box sx={{ maxWidth: "700px", margin: "0 auto", color: "#fff" }}>
      {/* ÊäïÁ®øË°®Á§∫ */}
      <Card sx={{ bgcolor: "#1e1e1e", mb: 3, p: 2 }}>
        <Box sx={{ display: "flex", alignItems: "center", mb: 1 }}>
          <Avatar src={post.communityIcon || ""} sx={{ mr: 1 }} />
          <Box>
            <Typography
              component={Link}
              to={`/r/${post.communitySlug}`}
              sx={{
                fontWeight: "bold",
                color: "#90caf9",
                textDecoration: "none",
              }}
            >
              @{post.communityName}
            </Typography>
            <Typography variant="body2" sx={{ color: "#aaa" }}>
              {post.username} „Éª{" "}
              {post.timestamp?.seconds &&
                new Date(post.timestamp.seconds * 1000).toLocaleString()}
            </Typography>
          </Box>
        </Box>

        <CardContent>
          <Typography variant="h6" sx={{ fontWeight: "bold", mb: 1 }}>
            {post.title}
          </Typography>
          <Typography variant="body1">{post.content}</Typography>
        </CardContent>

        {/* üëçüëé „Éú„Çø„É≥ */}
        <Box sx={{ display: "flex", gap: 2, mt: 1, ml: 1 }}>
          <IconButton
            size="small"
            sx={{
              color: post.likedBy?.includes(user?.uid) ? "#1976d2" : "#bbb",
            }}
            onClick={handleLikePost}
          >
            <ThumbUpIcon fontSize="small" />
            <Typography sx={{ ml: 0.5, fontSize: "0.8rem" }}>
              {post.likedBy?.length || 0}
            </Typography>
          </IconButton>

          <IconButton
            size="small"
            sx={{
              color: post.dislikedBy?.includes(user?.uid) ? "#d32f2f" : "#bbb",
            }}
            onClick={handleDislikePost}
          >
            <ThumbDownIcon fontSize="small" />
            <Typography sx={{ ml: 0.5, fontSize: "0.8rem" }}>
              {post.dislikedBy?.length || 0}
            </Typography>
          </IconButton>
        </Box>
      </Card>

      {/* „Ç≥„É°„É≥„ÉàÂÖ•Âäõ */}
      <Box sx={{ display: "flex", gap: 1, mb: 2 }}>
        <TextField
          fullWidth
          variant="outlined"
          placeholder="„Ç≥„É°„É≥„Éà„ÇíÂÖ•Âäõ..."
          value={comment}
          onChange={(e) => setComment(e.target.value)}
          onKeyDown={(e) => {
            if (e.key === "Enter" && !e.shiftKey) {
              e.preventDefault();
              handleComment();
            }
          }}
          sx={{ bgcolor: "#2c2c2c", input: { color: "#fff" } }}
        />
        <IconButton onClick={() => setComment("")} disabled={!comment}>
          <ClearIcon sx={{ color: "#aaa" }} />
        </IconButton>
        <Button variant="contained" onClick={handleComment}>
          ÈÄÅ‰ø°
        </Button>
      </Box>

      {/* „Ç≥„É°„É≥„Éà‰∏ÄË¶ß */}
      <Typography variant="h6" sx={{ mb: 1 }}>
        „Ç≥„É°„É≥„Éà‰∏ÄË¶ß
      </Typography>
      <Divider sx={{ mb: 2, bgcolor: "#444" }} />

      {comments.filter((c) => !c.parentId).length === 0 ? (
        <Typography sx={{ color: "#888" }}>„Åæ„Å†„Ç≥„É°„É≥„Éà„ÅØ„ÅÇ„Çä„Åæ„Åõ„Çì„ÄÇ</Typography>
      ) : (
        renderComments(null)
      )}
    </Box>
  );
}

// üîπ „Ç≥„É°„É≥„ÉàË°®Á§∫„Ç≥„É≥„Éù„Éº„Éç„É≥„Éà
function CommentItem({
  c,
  user,
  onLike,
  onDislike,
  onDelete,
  onEdit,
  replyingTo,
  setReplyingTo,
  replyText,
  setReplyText,
  onReplySubmit,
}: any) {
  const [isEditing, setIsEditing] = useState(false);
  const [editText, setEditText] = useState(c.text);

  return (
    <Box
      sx={{
        display: "flex",
        alignItems: "flex-start",
        bgcolor: "#1e1e1e",
        p: 1.5,
        borderRadius: "8px",
        mb: 1,
      }}
    >
      <Avatar src={c.avatarUrl} sx={{ mr: 1 }} />
      <Box sx={{ flexGrow: 1 }}>
        <Typography sx={{ fontWeight: "bold", fontSize: "0.9rem" }}>
          {c.username}
        </Typography>

        {isEditing ? (
          <Box>
            <TextField
              fullWidth
              variant="outlined"
              size="small"
              value={editText}
              onChange={(e) => setEditText(e.target.value)}
              sx={{ bgcolor: "#2c2c2c", input: { color: "#fff" }, mb: 1 }}
            />
            <Box sx={{ display: "flex", gap: 1 }}>
              <Button
                variant="contained"
                size="small"
                onClick={() => {
                  onEdit(c.id, editText);
                  setIsEditing(false);
                }}
              >
                ‰øùÂ≠ò
              </Button>
              <Button
                variant="outlined"
                size="small"
                onClick={() => setIsEditing(false)}
              >
                „Ç≠„É£„É≥„Çª„É´
              </Button>
            </Box>
          </Box>
        ) : (
          <Typography variant="body2" sx={{ color: "#ccc", mb: 0.5 }}>
            {c.text}
          </Typography>
        )}

        {/* üëçüëéüí¨ */}
        {!isEditing && (
          <Box sx={{ display: "flex", gap: 2, mt: 0.5 }}>
            <IconButton
              size="small"
              sx={{ color: c.likedBy?.includes(user?.uid) ? "#1976d2" : "#bbb" }}
              onClick={() => onLike(c.id, c.likedBy || [], c.dislikedBy || [])}
            >
              <ThumbUpIcon fontSize="small" />
              <Typography sx={{ ml: 0.5, fontSize: "0.8rem" }}>
                {c.likedBy?.length || 0}
              </Typography>
            </IconButton>

            <IconButton
              size="small"
              sx={{
                color: c.dislikedBy?.includes(user?.uid) ? "#d32f2f" : "#bbb",
              }}
              onClick={() =>
                onDislike(c.id, c.likedBy || [], c.dislikedBy || [])
              }
            >
              <ThumbDownIcon fontSize="small" />
              <Typography sx={{ ml: 0.5, fontSize: "0.8rem" }}>
                {c.dislikedBy?.length || 0}
              </Typography>
            </IconButton>

            <IconButton
              size="small"
              sx={{ color: "#bbb" }}
              onClick={() => setReplyingTo(c.id)}
            >
              <ChatBubbleOutlineIcon fontSize="small" />
              <Typography sx={{ ml: 0.5, fontSize: "0.8rem" }}>Ëøî‰ø°</Typography>
            </IconButton>
          </Box>
        )}

        {/* Ëøî‰ø°ÂÖ•Âäõ„Éï„Ç©„Éº„É† */}
        {replyingTo === c.id && (
          <Box sx={{ mt: 1 }}>
            <TextField
              fullWidth
              variant="outlined"
              size="small"
              placeholder={`@${c.username} „Å´Ëøî‰ø°...`}
              value={replyText}
              onChange={(e) => setReplyText(e.target.value)}
              sx={{ bgcolor: "#2c2c2c", input: { color: "#fff" }, mb: 1 }}
            />
            <Box sx={{ display: "flex", gap: 1 }}>
              <Button
                variant="contained"
                size="small"
                onClick={() => onReplySubmit(c.id)}
              >
                ÈÄÅ‰ø°
              </Button>
              <Button
                variant="outlined"
                size="small"
                onClick={() => setReplyingTo(null)}
              >
                „Ç≠„É£„É≥„Çª„É´
              </Button>
            </Box>
          </Box>
        )}
      </Box>

      {/* Ëá™ÂàÜ„ÅÆ„Ç≥„É°„É≥„Éà„Å†„ÅëÂâäÈô§„ÉªÁ∑®ÈõÜÂèØËÉΩ */}
      {user?.uid === c.uid && !isEditing && (
        <CommentOptions
          onDelete={() => onDelete(c.id)}
          onEdit={() => setIsEditing(true)}
        />
      )}
    </Box>
  );
}
